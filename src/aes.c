// ——— helpers ———
static inline uint8_t xtime(uint8_t x) {
    return (uint8_t) ((x << 1) ^ ((x & 0x80) ? 0x1B : 0x00));
}
static uint8_t gfmul(uint8_t a, uint8_t b) {  // slow but small; fine for labs
    uint8_t r = 0;
    for (; b; b >>= 1) {
        if (b & 1)
            r ^= a;
        a = xtime(a);
    }
    return r;
}
static void add_round_key(uint8_t s[16], const uint8_t* rk) {
    for (int i = 0; i < 16; i++)
        s[i] ^= rk[i];
}
static void sub_bytes(uint8_t s[16]) {
    for (int i = 0; i < 16; i++)
        s[i] = sbox[s[i]];
}
static void inv_sub_bytes(uint8_t s[16]) {
    for (int i = 0; i < 16; i++)
        s[i] = invsbox[s[i]];
}
static void shift_rows(uint8_t s[16]) {
    uint8_t t;
    // row1: (1,5,9,13) -> left 1
    t     = s[1];
    s[1]  = s[5];
    s[5]  = s[9];
    s[9]  = s[13];
    s[13] = t;
    // row2: (2,6,10,14) -> left 2
    uint8_t t0 = s[2], t1 = s[6];
    s[2]  = s[10];
    s[6]  = s[14];
    s[10] = t0;
    s[14] = t1;
    // row3: (3,7,11,15) -> left 3
    t     = s[3];
    s[3]  = s[15];
    s[15] = s[11];
    s[11] = s[7];
    s[7]  = t;
}
static void inv_shift_rows(uint8_t s[16]) {
    uint8_t t;
    // row1: right 1
    t     = s[13];
    s[13] = s[9];
    s[9]  = s[5];
    s[5]  = s[1];
    s[1]  = t;
    // row2: left 2 (self-inverse)
    uint8_t t0 = s[2], t1 = s[6];
    s[2]  = s[10];
    s[6]  = s[14];
    s[10] = t0;
    s[14] = t1;
    // row3: right 1
    t     = s[3];
    s[3]  = s[7];
    s[7]  = s[11];
    s[11] = s[15];
    s[15] = t;
}
static void mix_columns(uint8_t s[16]) {
    for (int c = 0; c < 4; c++) {
        int     i  = 4 * c;
        uint8_t a0 = s[i], a1 = s[i + 1], a2 = s[i + 2], a3 = s[i + 3];
        uint8_t r0 = (uint8_t) (gfmul(a0, 0x02) ^ gfmul(a1, 0x03) ^ a2 ^ a3);
        uint8_t r1 = (uint8_t) (a0 ^ gfmul(a1, 0x02) ^ gfmul(a2, 0x03) ^ a3);
        uint8_t r2 = (uint8_t) (a0 ^ a1 ^ gfmul(a2, 0x02) ^ gfmul(a3, 0x03));
        uint8_t r3 = (uint8_t) (gfmul(a0, 0x03) ^ a1 ^ a2 ^ gfmul(a3, 0x02));
        s[i]       = r0;
        s[i + 1]   = r1;
        s[i + 2]   = r2;
        s[i + 3]   = r3;
    }
}
static void inv_mix_columns(uint8_t s[16]) {
    for (int c = 0; c < 4; c++) {
        int     i  = 4 * c;
        uint8_t a0 = s[i], a1 = s[i + 1], a2 = s[i + 2], a3 = s[i + 3];
        uint8_t r0 = (uint8_t) (gfmul(a0, 0x0e) ^ gfmul(a1, 0x0b)
                                ^ gfmul(a2, 0x0d) ^ gfmul(a3, 0x09));
        uint8_t r1 = (uint8_t) (gfmul(a0, 0x09) ^ gfmul(a1, 0x0e)
                                ^ gfmul(a2, 0x0b) ^ gfmul(a3, 0x0d));
        uint8_t r2 = (uint8_t) (gfmul(a0, 0x0d) ^ gfmul(a1, 0x09)
                                ^ gfmul(a2, 0x0e) ^ gfmul(a3, 0x0b));
        uint8_t r3 = (uint8_t) (gfmul(a0, 0x0b) ^ gfmul(a1, 0x0d)
                                ^ gfmul(a2, 0x09) ^ gfmul(a3, 0x0e));
        s[i]       = r0;
        s[i + 1]   = r1;
        s[i + 2]   = r2;
        s[i + 3]   = r3;
    }
}

// ——— Key expansion (AES-128) ———
void aes128_key_expand(uint8_t rk[176], const uint8_t key[16]) {
    // Byte-oriented expand per FIPS-197 §5.2; rk holds 11*16 bytes
    memcpy(rk, key, 16);
    uint8_t t[4];
    for (int i = 16, r = 1; i < 176; i += 4) {
        t[0] = rk[i - 4];
        t[1] = rk[i - 3];
        t[2] = rk[i - 2];
        t[3] = rk[i - 1];
        if ((i % 16) == 0) {
            // RotWord, SubWord, Rcon
            uint8_t tmp = t[0];
            t[0]        = t[1];
            t[1]        = t[2];
            t[2]        = t[3];
            t[3]        = tmp;
            t[0]        = sbox[t[0]];
            t[1]        = sbox[t[1]];
            t[2]        = sbox[t[2]];
            t[3]        = sbox[t[3]];
            t[0] ^= Rcon[r++];
        }
        rk[i + 0] = rk[i - 16 + 0] ^ t[0];
        rk[i + 1] = rk[i - 16 + 1] ^ t[1];
        rk[i + 2] = rk[i - 16 + 2] ^ t[2];
        rk[i + 3] = rk[i - 16 + 3] ^ t[3];
    }
}

// ——— Block cipher ———
void aes128_encrypt_block(uint8_t       out[16],
                          const uint8_t in[16],
                          const uint8_t rk[176]) {
    uint8_t s[16];
    memcpy(s, in, 16);
    add_round_key(s, rk);  // round 0
    for (int r = 1; r <= 9; r++) {
        sub_bytes(s);
        shift_rows(s);
        mix_columns(s);
        add_round_key(s, rk + 16 * r);
    }
    sub_bytes(s);
    shift_rows(s);
    add_round_key(s, rk + 160);  // round 10
    memcpy(out, s, 16);
}

void aes128_decrypt_block(uint8_t       out[16],
                          const uint8_t in[16],
                          const uint8_t rk[176]) {
    uint8_t s[16];
    memcpy(s, in, 16);
    add_round_key(s, rk + 160);  // round 10
    for (int r = 9; r >= 1; r--) {
        inv_shift_rows(s);
        inv_sub_bytes(s);
        add_round_key(s, rk + 16 * r);
        inv_mix_columns(s);
    }
    inv_shift_rows(s);
    inv_sub_bytes(s);
    add_round_key(s, rk);  // round 0
    memcpy(out, s, 16);
}

// ——— CBC mode (no padding) ———
int cbc_encrypt(uint8_t*       dst,
                const uint8_t* src,
                size_t         n,
                const uint8_t  iv[16],
                const uint8_t  rk[176]) {
    if (!dst || !src || !iv || !rk)
        return -1;
    if (n % AES_BLK)
        return -1;
    uint8_t prev[16], block[16];
    memcpy(prev, iv, 16);
    for (size_t i = 0; i < n; i += AES_BLK) {
        for (int j = 0; j < 16; j++)
            block[j] = src[i + j] ^ prev[j];
        aes128_encrypt_block(dst + i, block, rk);
        memcpy(prev, dst + i, 16);
    }
    return 0;
}

int cbc_decrypt(uint8_t*       dst,
                const uint8_t* src,
                size_t         n,
                const uint8_t  iv[16],
                const uint8_t  rk[176]) {
    if (!dst || !src || !iv || !rk)
        return -1;
    if (n % AES_BLK)
        return -1;
    uint8_t prev[16], block[16];
    memcpy(prev, iv, 16);
    for (size_t i = 0; i < n; i += AES_BLK) {
        aes128_decrypt_block(block, src + i, rk);
        for (int j = 0; j < 16; j++)
            dst[i + j] = block[j] ^ prev[j];
        memcpy(prev, src + i, 16);
    }
    return 0;
}

// ——— PKCS#7 (CMS) ———
int pkcs7_pad(uint8_t** out, size_t* on, const uint8_t* in, size_t n) {
    if (!out || !on || (!in && n))
        return -1;
    size_t pad = AES_BLK - (n % AES_BLK);
    if (pad == 0)
        pad = AES_BLK;
    size_t   m   = n + pad;
    uint8_t* buf = (uint8_t*) malloc(m);
    if (!buf)
        return -1;
    if (in && n)
        memcpy(buf, in, n);
    memset(buf + n, (int) pad, pad);
    *out = buf;
    *on  = m;
    return 0;
}

int pkcs7_unpad(uint8_t* buf, size_t* n) {
    if (!buf || !n || (*n % AES_BLK) != 0)
        return -1;
    size_t  len = *n;
    uint8_t pad = buf[len - 1];
    if (pad == 0 || pad > AES_BLK)
        return -1;
    // constant-time-ish check
    uint8_t bad = 0;
    for (size_t i = 0; i < pad; i++)
        bad |= (uint8_t) (buf[len - 1 - i] ^ pad);
    if (bad)
        return -1;
    *n = len - pad;
    return 0;
}

